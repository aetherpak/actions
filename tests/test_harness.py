def create_mock_app_repo(repo_path: pathlib.Path, app_id: str, version: str = "1.0") -> None:
    """Build a minimal valid Flatpak OSTree repository containing a mock app structure."""
    if repo_path.exists():
        shutil.rmtree(repo_path)
    subprocess.run(["ostree", "init", "--mode=archive", f"--repo={repo_path}"], check=True)

    with tempfile.TemporaryDirectory() as tmpdir_app, tempfile.TemporaryDirectory() as tmpdir_repo:
        app_dir = pathlib.Path(tmpdir_app)
        tmp_repo = pathlib.Path(tmpdir_repo) / "repo"

        files_dir = app_dir / "files"
        files_dir.mkdir(parents=True)
        (files_dir / "hello").write_text(f"echo 'Hello version {version}'\n")
        (files_dir / "hello").chmod(0o755)

        metadata = (
            "[Application]\n"
            f"name={app_id}\n"
            "runtime=org.freedesktop.Platform/x86_64/23.08\n"
            "sdk=org.freedesktop.Sdk/x86_64/23.08\n"
            "command=hello\n"
        )
        (app_dir / "metadata").write_text(metadata)

        # We need to write the commit to a temporary repo first, then use
        # flatpak build-commit-from to commit it to the target repo with correct ref-binding.
        subprocess.run(["ostree", "init", "--mode=archive", f"--repo={tmp_repo}"], check=True)

        subprocess.run(
            [
                "ostree",
                "commit",
                f"--repo={tmp_repo}",
                "--branch=temp-branch",
                "--owner-uid=0",
                "--owner-gid=0",
                "--canonical-permissions",
                f"--add-metadata-string=xa.metadata={metadata}",
                "-s",
                "Mock application commit",
                str(app_dir),
            ],
            check=True,
            capture_output=True,
        )

        subprocess.run(
            [
                "flatpak",
                "build-commit-from",
                f"--src-repo={tmp_repo}",
                "--src-ref=temp-branch",
                "--no-update-summary",
                str(repo_path),
                f"app/{app_id}/x86_64/stable",
            ],
            check=True,
            capture_output=True,
        )


def push_to_registry(
    repo_path: pathlib.Path, app_id: str, registry_port: int, image_name: str, tag: str
) -> str:
    """Convert OSTree to OCI bundle and push to the local OCI registry."""
    with tempfile.TemporaryDirectory() as tmpdir:
        oci_dir = pathlib.Path(tmpdir) / "oci"
        subprocess.run(
            [
                "flatpak",
                "build-bundle",
                "--oci",
                "--arch=x86_64",
                str(repo_path),
                str(oci_dir),
                app_id,
                "stable",
            ],
            check=True,
            capture_output=True,
        )

        subprocess.run(
            [
                "skopeo",
                "copy",
                "--dest-tls-verify=false",
                "--all",
                f"oci:{oci_dir}",
                f"docker://localhost:{registry_port}/{image_name}:{tag}",
            ],
            check=True,
            capture_output=True,
        )

        # Query digest
        res = subprocess.run(
            [
                "skopeo",
                "inspect",
                "--tls-verify=false",
                f"docker://localhost:{registry_port}/{image_name}:{tag}",
            ],
            check=True,
            capture_output=True,
            text=True,
        )
        return json.loads(res.stdout)["Digest"]


def run_indexing(
    site_dir: pathlib.Path,
    registry_port: int,
    image_name: str,
    digest: str,
    app_id: str,
    tag: str,
    insecure: bool = True,
) -> None:
    """Execute merge_index.py to insert the OCI coordinates into index/static."""
    index_path = site_dir / "index" / "static"
    # Match the production insecure-registry scheme mapping: http when insecure, else https
    scheme = "http" if insecure else "https"
    registry_url = f"{scheme}://localhost:{registry_port}"

    # Resolve the correct OCI tag to query the registry with
    if "-" in tag:
        oci_tag = tag
    else:
        oci_tag = f"{app_id.replace('.', '_')}-{tag}-x86_64"

    # We resolve labels from the local skopeo inspect
    res = subprocess.run(
        [
            "skopeo",
            "inspect",
            "--tls-verify=false",
            f"docker://localhost:{registry_port}/{image_name}:{oci_tag}",
        ],
        check=True,
        capture_output=True,
        text=True,
    )
    labels = json.loads(res.stdout).get("Labels", {})
    labels_file = site_dir / "labels.json"
    labels_file.write_text(json.dumps(labels))

    subprocess.run(
        [
            "python3",
            str(REPO_ROOT / "publish" / "merge_index.py"),
            "--index-path",
            str(index_path),
            "--registry",
            registry_url,
            "--name",
            image_name,
            "--digest",
            digest,
            "--ref",
            f"app/{app_id}/x86_64/stable",
            "--labels-file",
            str(labels_file),
            "--tag",
            oci_tag,
            "--arch",
            "x86_64",
        ],
        check=True,
    )


@pytest.mark.integration
def test_scenario1_manifest_flow(
    test_dir: pathlib.Path, registry_server: int, web_server: int, flatpak_user_dir: pathlib.Path
) -> None:
    """Scenario 1: Single-App Manifest-Based Build & Install."""
    app_id = "org.flatpak.MockApp"
    repo_path = test_dir / "repo"
    site_dir = test_dir / "site"
    image_name = "aetherpak/mock-app"
    tag = "org_flatpak_MockApp-stable-x86_64"

    # 1. Build app
    create_mock_app_repo(repo_path, app_id)

    # 2. Push to local registry
    digest = push_to_registry(repo_path, app_id, registry_server, image_name, tag)

    # 3. Index and generate index/static
    run_indexing(site_dir, registry_server, image_name, digest, app_id, tag)

    # 4. Generate .flatpakrepo and ref files
    remote_name = "mock-remote"
    subprocess.run(
        [
            "python3",
            str(REPO_ROOT / "publish" / "gen_flatpakrefs.py"),
            "--index-path",
            str(site_dir / "index" / "static"),
            "--out-dir",
            str(site_dir / "refs"),
            "--url",
            f"oci+http://localhost:{web_server}",
            "--remote-name",
            remote_name,
            "--runtime-repo",
            "https://dl.flathub.org/repo/flathub.flatpakrepo",
        ],
        check=True,
    )

    # 5. Client install and verification
    subprocess.run(
        [
            "flatpak",
            "remote-add",
            "--user",
            "--no-gpg-verify",
            remote_name,
            f"oci+http://localhost:{web_server}",
        ],
        check=True,
    )

    # Verify app shows in remote-ls
    ls_res = subprocess.run(
        ["flatpak", "remote-ls", "--user", remote_name], check=True, capture_output=True, text=True
    )
    assert app_id in ls_res.stdout

    # Install --no-deps to skip pulling external runtime from Flathub
    subprocess.run(
        ["flatpak", "install", "--user", "-y", "--no-deps", remote_name, app_id], check=True
    )

    # Verify installation
    info_res = subprocess.run(
        ["flatpak", "info", "--user", app_id], check=True, capture_output=True, text=True
    )
    assert app_id in info_res.stdout

    # Uninstall & remote-delete
    subprocess.run(["flatpak", "uninstall", "--user", "-y", app_id], check=True)
    subprocess.run(["flatpak", "remote-delete", "--user", remote_name], check=True)


