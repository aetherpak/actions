@pytest.mark.integration
def test_scenario5_delisting_reconcile(
    test_dir: pathlib.Path,
    registry_server: int,
    web_server: int,
    flatpak_user_dir: pathlib.Path,
    container_manager: str,
) -> None:
    """Scenario 5: Reconciliation & Delisting verification."""
    app_a = "org.flatpak.AppA"
    app_b = "org.flatpak.AppB"
    site_dir = test_dir / "site"

    # 1. Deploy both app-a and app-b
    repo_a = test_dir / "repo_a"
    create_mock_app_repo(repo_a, app_a)
    digest_a = push_to_registry(
        repo_a, app_a, registry_server, "aetherpak/app-a", "org_flatpak_AppA-stable-x86_64"
    )

    repo_b = test_dir / "repo_b"
    create_mock_app_repo(repo_b, app_b)
    digest_b = push_to_registry(
        repo_b, app_b, registry_server, "aetherpak/app-b", "org_flatpak_AppB-stable-x86_64"
    )

    run_indexing(site_dir, registry_server, "aetherpak/app-a", digest_a, app_a, "stable")
    run_indexing(site_dir, registry_server, "aetherpak/app-b", digest_b, app_b, "stable")

    # Confirm both are initially serving
    remote_name = "reconcile-remote"
    subprocess.run(
        [
            "flatpak",
            "remote-add",
            "--user",
            "--no-gpg-verify",
            remote_name,
            f"oci+http://localhost:{web_server}",
        ],
        check=True,
    )
    ls_res = subprocess.run(
        ["flatpak", "remote-ls", "--user", remote_name], check=True, capture_output=True, text=True
    )
    assert app_a in ls_res.stdout
    assert app_b in ls_res.stdout

    # 2. Simulate de-listing app-b by deleting it from the registry
    subprocess.run(
        [
            "skopeo",
            "delete",
            "--tls-verify=false",
            f"docker://localhost:{registry_server}/aetherpak/app-b:org_flatpak_AppB-stable-x86_64",
        ],
        check=True,
    )

    # 3. Run reconcile.py. It will check "aetherpak/app-b" presence, fail to locate it, and drop it.
    subprocess.run(
        [
            "python3",
            str(REPO_ROOT / "publish" / "reconcile.py"),
            "--index-path",
            str(site_dir / "index" / "static"),
            "--registry",
            f"http://localhost:{registry_server}",
            "--insecure",
        ],
        check=True,
    )

    # 4. Verify index static content
    index_data = json.loads((site_dir / "index" / "static").read_text())
    active_names = [r["Name"] for r in index_data["Results"]]
    assert "aetherpak/app-a" in active_names
    assert "aetherpak/app-b" not in active_names

    # 5. Flush client-side cache and verify
    subprocess.run(["flatpak", "remote-delete", "--user", remote_name], check=True)
    subprocess.run(
        [
            "flatpak",
            "remote-add",
            "--user",
            "--no-gpg-verify",
            remote_name,
            f"oci+http://localhost:{web_server}",
        ],
        check=True,
    )

    ls_reconciled = subprocess.run(
        ["flatpak", "remote-ls", "--user", remote_name], check=True, capture_output=True, text=True
    )
    assert app_a in ls_reconciled.stdout
    assert app_b not in ls_reconciled.stdout

    # Install app-a passes, app-b fails
    subprocess.run(
        ["flatpak", "install", "--user", "-y", "--no-deps", remote_name, app_a], check=True
    )
    res_b = subprocess.run(
        ["flatpak", "install", "--user", "-y", "--no-deps", remote_name, app_b],
        capture_output=True,
        text=True,
    )
    assert res_b.returncode != 0
