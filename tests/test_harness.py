@pytest.mark.integration
def test_scenario3_gpg_signing_enforcement(
    test_dir: pathlib.Path,
    registry_server: int,
    web_server: int,
    flatpak_user_dir: pathlib.Path,
    gpg_key: tuple[str, str],
) -> None:
    """Scenario 3: GPG-Signed Deployment Flow & Signature Enforcement."""
    fpr, priv_key = gpg_key
    app_id = "org.flatpak.SignedApp"
    repo_path = test_dir / "repo"
    site_dir = test_dir / "site"
    image_name = "aetherpak/signed-app"
    tag = "org_flatpak_SignedApp-stable-x86_64"

    create_mock_app_repo(repo_path, app_id)

    # Sign during skopeo push
    # We stage registries.d YAML mapping local host to our site's sigs dir
    sigs_dir = site_dir / "sigs"
    sigs_dir.mkdir(parents=True, exist_ok=True)

    # Write registries.d
    regd_dir = test_dir / "registries.d"
    regd_dir.mkdir(exist_ok=True)
    (regd_dir / "aetherpak.yaml").write_text(
        f"docker:\n  localhost:{registry_server}:\n    lookaside-staging: file://{sigs_dir.resolve()}\n"
    )

    # Import private key to a temp GNUPGHOME for the push step
    gnupghome = test_dir / "gpg_temp"
    gnupghome.mkdir(exist_ok=True)
    gnupghome.chmod(0o700)
    env = {**os.environ, "GNUPGHOME": str(gnupghome)}
    subprocess.run(
        ["gpg", "--batch", "--import"],
        input=priv_key,
        env=env,
        check=True,
        capture_output=True,
        text=True,
    )

    # Convert and push with signing
    with tempfile.TemporaryDirectory() as tmpdir:
        oci_dir = pathlib.Path(tmpdir) / "oci"
        subprocess.run(
            [
                "flatpak",
                "build-bundle",
                "--oci",
                "--arch=x86_64",
                str(repo_path),
                str(oci_dir),
                app_id,
                "stable",
            ],
            check=True,
            capture_output=True,
        )

        subprocess.run(
            [
                "skopeo",
                f"--registries.d={regd_dir}",
                "copy",
                "--dest-tls-verify=false",
                f"--sign-by={fpr}",
                "--all",
                f"oci:{oci_dir}",
                f"docker://localhost:{registry_server}/{image_name}:{tag}",
            ],
            env=env,
            check=True,
            capture_output=True,
        )

    res = subprocess.run(
        [
            "skopeo",
            "inspect",
            "--tls-verify=false",
            f"docker://localhost:{registry_server}/{image_name}:{tag}",
        ],
        check=True,
        capture_output=True,
        text=True,
    )
    digest = json.loads(res.stdout)["Digest"]

    # Export public key (ASCII armored for flatpak client import)
    pub_res = subprocess.run(
        ["gpg", "--armor", "--export", fpr], env=env, check=True, capture_output=True, text=True
    )
    (sigs_dir / "key.asc").write_text(pub_res.stdout)

    # Export binary public key and base64-encode it for gen_flatpakrefs.py
    pub_bin_res = subprocess.run(["gpg", "--export", fpr], env=env, check=True, capture_output=True)
    gpg_key_b64 = base64.b64encode(pub_bin_res.stdout).decode("utf-8")
    (sigs_dir / "key.b64").write_text(gpg_key_b64)

    # Write signing.json manifest
    subprocess.run(
        [
            "python3",
            str(REPO_ROOT / "publish" / "signing.py"),
            "manifest",
            "--out",
            str(sigs_dir / "signing.json"),
            "--sig-dir",
            "sigs",
            "--fingerprint",
            fpr,
            "--remote-name",
            "mock-signed-remote",
        ],
        check=True,
    )

    run_indexing(site_dir, registry_server, image_name, digest, app_id, tag)

    # Generate ref file with embedded GPG key + lookaside
    subprocess.run(
        [
            "python3",
            str(REPO_ROOT / "publish" / "gen_flatpakrefs.py"),
            "--index-path",
            str(site_dir / "index" / "static"),
            "--out-dir",
            str(site_dir / "refs"),
            "--url",
            f"oci+http://localhost:{web_server}",
            "--remote-name",
            "mock-signed-remote",
            "--runtime-repo",
            "https://dl.flathub.org/repo/flathub.flatpakrepo",
            "--gpg-key-base64-file",
            str(sigs_dir / "key.b64"),
            "--signature-lookaside",
            f"http://localhost:{web_server}/sigs",
        ],
        check=True,
    )

    # Verify verified install (GPG verification enabled)
    remote_name = "mock-signed-remote"
    run_command_with_log(
        [
            "flatpak",
            "remote-add",
            "--user",
            f"--gpg-import={sigs_dir / 'key.asc'}",
            remote_name,
            f"oci+http://localhost:{web_server}",
        ]
    )
    set_remote_signature_lookaside(
        flatpak_user_dir, remote_name, f"http://localhost:{web_server}/sigs"
    )

    # Install should pass verification
    subprocess.run(
        ["flatpak", "install", "--user", "-y", "--no-deps", remote_name, app_id], check=True
    )
    info_res = subprocess.run(
        ["flatpak", "info", "--user", app_id], check=True, capture_output=True, text=True
    )
    assert app_id in info_res.stdout

    # Clean client sandbox to verify enforcement on fresh install
    subprocess.run(["flatpak", "uninstall", "--user", "-y", app_id], check=True)
    subprocess.run(["flatpak", "remote-delete", "--user", remote_name], check=True)
    shutil.rmtree(flatpak_user_dir)
    flatpak_user_dir.mkdir()

    # TAMPER with the signature file
    sig_rel = f"sigs/{image_name}@{digest.replace(':', '=')}/signature-1"
    sig_file = site_dir / sig_rel
    assert sig_file.is_file(), f"Signature file does not exist at {sig_file}"
    sig_file.write_text("TAMPERED_SIGNATURE_BYTES")

    # Re-add remote with key and lookaside
    run_command_with_log(
        [
            "flatpak",
            "remote-add",
            "--user",
            f"--gpg-import={sigs_dir / 'key.asc'}",
            remote_name,
            f"oci+http://localhost:{web_server}",
        ]
    )
    set_remote_signature_lookaside(
        flatpak_user_dir, remote_name, f"http://localhost:{web_server}/sigs"
    )

    # Install MUST fail now due to tampered signature
    res_tamper = subprocess.run(
        ["flatpak", "install", "--user", "-y", "--no-deps", remote_name, app_id],
        capture_output=True,
        text=True,
    )

    assert res_tamper.returncode != 0
    assert (
        "signature" in res_tamper.stderr.lower()
        or "verify" in res_tamper.stderr.lower()
        or "valid" in res_tamper.stderr.lower()
    )


